use crate::routes::State;
use utoipa_axum::{router::OpenApiRouter, routes};

pub fn router(state: &State) -> OpenApiRouter<State> {
    OpenApiRouter::new()
        .routes(routes!(crate::controllers::sites::list))
        .routes(routes!(crate::controllers::sites::create))
        .routes(routes!(crate::controllers::sites::get))
        .routes(routes!(crate::controllers::sites::destroy))
        .routes(routes!(crate::controllers::sites::deploy))
        .routes(routes!(crate::controllers::sites::backup_create))
        .routes(routes!(crate::controllers::sites::backup_list))
        .routes(routes!(crate::controllers::sites::backup_restore))
        .routes(routes!(crate::controllers::sites::templates_list))
        .routes(routes!(crate::controllers::sites::logs))
        .routes(routes!(crate::controllers::sites::databases_list))
        .routes(routes!(crate::controllers::sites::databases_create))
        .routes(routes!(crate::controllers::sites::databases_destroy))
        .routes(routes!(crate::controllers::sites::tasks_list))
        .routes(routes!(crate::controllers::sites::tasks_create))
        .routes(routes!(crate::controllers::sites::tasks_run))
        .routes(routes!(crate::controllers::sites::tasks_destroy))
        .routes(routes!(crate::controllers::sites::dns_list))
        .routes(routes!(crate::controllers::sites::dns_sync))
        .routes(routes!(crate::controllers::sites::mail_status))
        .routes(routes!(crate::controllers::sites::mail_accounts_list))
        .routes(routes!(crate::controllers::sites::mail_accounts_create))
        .routes(routes!(crate::controllers::sites::mail_accounts_destroy))
        .routes(routes!(crate::controllers::sites::mail_sync))
        .routes(routes!(crate::controllers::sites::ftp_status))
        .routes(routes!(crate::controllers::sites::ftp_accounts_list))
        .routes(routes!(crate::controllers::sites::ftp_accounts_create))
        .routes(routes!(crate::controllers::sites::ftp_accounts_destroy))
        .routes(routes!(crate::controllers::sites::ftp_sync))
        .routes(routes!(crate::controllers::sites::exec))
        .routes(routes!(crate::controllers::sites::hosting_overview))
        .routes(routes!(crate::controllers::sites::hosting_limits))
        .routes(routes!(crate::controllers::sites::site_stats))
        .routes(routes!(crate::controllers::sites::files_list))
        .routes(routes!(crate::controllers::sites::files_download))
        .routes(routes!(crate::controllers::sites::files_upload))
        .routes(routes!(crate::controllers::sites::files_delete))
        .routes(routes!(crate::controllers::sites::files_mkdir))
        .routes(routes!(crate::controllers::sites::files_rename))
        .routes(routes!(crate::controllers::sites::files_move))
        .routes(routes!(crate::controllers::sites::files_copy))
        .routes(routes!(crate::controllers::sites::files_archive))
        .routes(routes!(crate::controllers::sites::webhook_deploy))
        .with_state(state.clone())
}
